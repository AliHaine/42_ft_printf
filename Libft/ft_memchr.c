#include "libft.h"

void	*ft_memchr(void *s1, int c, size_t size)
{
	size_t	i;

	i = 0;
	while (i < size)
	{
		if (((char *)s1)[i] == (char) c)
			return (&s1[i]);
		i++;
	}
	return (0);
}
