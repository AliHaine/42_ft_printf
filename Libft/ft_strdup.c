#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dst;
	int		i;

	dst = NULL;
	dst = malloc(sizeof(char) * (ft_strlen(((char *)s)) + 1));
	if (dst == NULL)
		return (0);
	i = 0;
	while (s[i])
	{
		dst[i] = s[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
